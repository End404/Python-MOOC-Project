print("多分支：...>= 60,70,80,90...")
sc = eval(input("3.0输入："))
if sc >= 90:
    gr = "D"
elif sc >= 80:
    gr = "C"
elif sc >= 70:
    gr = "B"
elif sc >= 60:
    gr = "A"
print ("输入成绩属于级别 {} ！" .format( gr ))     # 成绩级别不准啊。顺序改了就准了。
