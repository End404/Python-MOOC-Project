# ------------------ 条件组合 --------------------
# ---- 用于条件组合的三个保留字：and,or,not。
# ---- X and Y :两个条件X和Y的逻辑与。
# ---- X or Y :两个条件X和Y逻辑或。
# ---- not X : 条件X的逻辑非。
# ------------------------------------------------#
print ('条件组合：...99...')
gu = eval(input('输入：'))  # 输入的数字作为判断条件。
if gu > 99 or gu < 99:
    print ('猜错了')    # 大于
else :
    print ("猜对了")    # 小雨

