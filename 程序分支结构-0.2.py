# --------- 二分支；紧凑形式：适用于简单表达式的二分支结构。 --------- 
# --------- <表达式> if <条件> else <表达式2> --------------------- 

print ("紧凑形式：...0--99--")
gu = eval(input('2.5输入：'))
print ('猜{}了'.format("dui" if gu==99 else 'cuo'))

